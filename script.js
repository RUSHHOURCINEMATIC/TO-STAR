function syncEnterBtnState() {
  if (agreeCheckbox.checked) {
    enterBtn.disabled = false;
    enterBtn.classList.add('active');
  } else {
    enterBtn.disabled = true;
    enterBtn.classList.remove('active');
  }
}

agreeCheckbox.addEventListener('change', syncEnterBtnState);
agreeCheckbox.addEventListener('click', syncEnterBtnState);
// 혹시 페이지 로드 시점에 브라우저가 checkbox 상태를 복원해두는 경우 대비
syncEnterBtnState();

enterBtn.addEventListener('click', () => {
  if (!agreeCheckbox.checked) return; // 체크 안 됐으면 무시
  agreeView.style.display = 'none';
  pinView.style.display = 'block';
  pinDigits[0].focus();
});

// 핀 입력 자동 이동
pinDigits.forEach((input, idx) => {
  input.addEventListener('input', () => {
    input.value = input.value.replace(/[^0-9]/g, '');
    if (input.value.length === 1 && idx < pinDigits.length - 1) {
      pinDigits[idx + 1].focus();
    }
  });
  input.addEventListener('keydown', (e) => {
    if (e.key === 'Backspace' && input.value === '' && idx > 0) {
      pinDigits[idx - 1].focus();
    }
    if (e.key === 'Enter') {
      pinSubmitBtn.click();
    }
  });
});

pinSubmitBtn.addEventListener('click', async () => {
  const entered = Array.from(pinDigits).map(i => i.value).join('');
  if (entered.length < 4) {
    pinError.textContent = '4자리 암호를 모두 입력해주세요.';
    return;
  }
  const todayCode = await getTodayCode();
  if (entered === todayCode) {
    pinError.textContent = '';
    unlockSite();
  } else {
    pinError.textContent = '암호가 올바르지 않습니다. 스태프에게 다시 문의해주세요.';
    pinDigits.forEach(i => i.value = '');
    pinDigits[0].focus();
  }
});

function unlockSite() {
  gateOverlay.style.display = 'none';
  document.body.style.overflow = 'hidden'; // 인트로 재생 중 스크롤 방지
  playIntro();
}

