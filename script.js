const trackItems = document.querySelectorAll('.track-item');

let currentTrack = null;
let isSeeking = false;

function formatTime(sec) {
  if (isNaN(sec)) return '0:00';
  const m = Math.floor(sec / 60);
  const s = Math.floor(sec % 60);
  return `${m}:${s.toString().padStart(2, '0')}`;
}

function loadTrack(idx) {
  currentTrack = idx;
  audioPlayer.src = TRACK_FILES[idx];
  playerTitle.textContent = `${String(idx+1).padStart(2,'0')}. ${TRACK_TITLES[idx]}`;
  playerBar.classList.add('active');
  lyricsBox.classList.remove('open');
  lyricsBox.textContent = LYRICS[idx] || '';
  lyricsToggleBtn.textContent = '가사 보기';

  trackItems.forEach(item => {
    item.classList.toggle('playing', parseInt(item.dataset.track) === idx);
  });

  audioPlayer.play();
  playPauseBtn.textContent = '❚❚';

  playerBar.scrollIntoView({ behavior: 'smooth', block: 'center' });
}

trackItems.forEach(item => {
  item.addEventListener('click', () => {
    const idx = parseInt(item.dataset.track);
    if (currentTrack === idx) {
      togglePlayPause();
    } else {
      loadTrack(idx);
    }
  });
});

function togglePlayPause() {
  if (audioPlayer.paused) {
    audioPlayer.play();
    playPauseBtn.textContent = '❚❚';
  } else {
    audioPlayer.pause();
    playPauseBtn.textContent = '▶';
  }
}
playPauseBtn.addEventListener('click', togglePlayPause);

audioPlayer.addEventListener('timeupdate', () => {
  if (isSeeking) return;
  const pct = (audioPlayer.currentTime / audioPlayer.duration) * 100 || 0;
  seekBar.value = pct;
  timeLabel.textContent = `${formatTime(audioPlayer.currentTime)} / ${formatTime(audioPlayer.duration)}`;
});

seekBar.addEventListener('input', () => { isSeeking = true; });
seekBar.addEventListener('change', () => {
  const newTime = (seekBar.value / 100) * audioPlayer.duration;
  audioPlayer.currentTime = newTime;
  isSeeking = false;
});

audioPlayer.addEventListener('ended', () => {
  playPauseBtn.textContent = '▶';
  seekBar.value = 0;
});

lyricsToggleBtn.addEventListener('click', () => {
  const isOpen = lyricsBox.classList.toggle('open');
  lyricsToggleBtn.textContent = isOpen ? '가사 닫기' : '가사 보기';
});
